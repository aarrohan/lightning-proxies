import { useEffect, useRef, useState } from "react";

interface ILanguage {
  icon: JSX.Element;
  name: string;
}

interface IServiceSelectOption {
  value: string;
  title: string;
}

interface IServiceSelect {
  selectedServiceIndex: number;
  setSelectedServiceIndex: (index: number) => void;
  options: IServiceSelectOption[];
}

function ServiceSelect({
  selectedServiceIndex,
  setSelectedServiceIndex,
  options,
}: IServiceSelect) {
  const [isOpened, setIsOpened] = useState<boolean>(false);

  const ref = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const handleOutsideClick = (event: MouseEvent) => {
      if (ref.current && !ref.current.contains(event.target as Node)) {
        setIsOpened(false);
      }
    };

    document.addEventListener("mousedown", handleOutsideClick);

    return () => {
      document.removeEventListener("mousedown", handleOutsideClick);
    };
  }, []);

  return (
    <div className="relative">
      <div
        onClick={() => setIsOpened(!isOpened)}
        className="py-2.5 px-5 border border-white/10 bg-[#011736] rounded-md flex justify-between items-center cursor-pointer"
      >
        <p className="text-xs sm:text-sm font-medium text-white">
          {options[selectedServiceIndex].title}
        </p>

        <svg
          width="10"
          height="7"
          viewBox="0 0 10 7"
          fill="none"
          xmlns="http://www.w3.org/2000/svg"
          className={`${isOpened ? "transform rotate-180" : ""} duration-200`}
        >
          <path
            fillRule="evenodd"
            clipRule="evenodd"
            d="M9.78033 0.96967C10.0732 1.26256 10.0732 1.73744 9.78033 2.03033L5.78033 6.03033C5.48744 6.32322 5.01256 6.32322 4.71967 6.03033L0.71967 2.03033C0.426777 1.73744 0.426777 1.26256 0.71967 0.96967C1.01256 0.676777 1.48744 0.676777 1.78033 0.96967L5.25 4.43934L8.71967 0.96967C9.01256 0.676777 9.48744 0.676777 9.78033 0.96967Z"
            fill="white"
          />
        </svg>
      </div>

      <div
        ref={ref}
        className={`absolute z-20 top-full left-0 ${
          !isOpened ? "scale-75" : ""
        } origin-top w-full pt-2 ${
          !isOpened ? "opacity-0 pointer-events-none" : ""
        } duration-300`}
      >
        <div className="border border-white/10 bg-[#011736] rounded-md flex flex-col overflow-hidden">
          {options.map((option, index) => {
            return (
              <button
                key={index}
                onClick={() => {
                  setSelectedServiceIndex(index);
                  setIsOpened(false);
                }}
                className={`py-2.5 px-5 ${
                  index !== options.length - 1 ? "border-b border-white/10" : ""
                } hover:bg-white/5 text-xs sm:text-sm font-medium text-left text-white duration-200`}
              >
                {option.title}
              </button>
            );
          })}
        </div>
      </div>
    </div>
  );
}

